function update() {
    if (step == -1) {
        step = 0;
        return;
    }else if(step > 9){
        step = 9;
        return;
    }
    text.textContent = questions[step];
    progr.textContent = "" + (step + 1) + "\\" + 10;
    progressBar.style.width = (step + 1)* 10 + "%";
    img.src = "imgs/"+level+"/"+step+".png";
    for (let i = 0; i < 4; i++) {
        if(selected[step] == i){
            option[i].className = "selected option";
        }else{
            option[i].className = "option";
        }
        option[i].textContent = options[step][i];
    }
}
