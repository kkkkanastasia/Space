function optionClicked(i) {
    if(selected[step] == i){   
        option[i].className = "option";
        selected[step] = -1;
    }else{
        option[i].className = "selected option";
        if(selected[step] != -1){
            option[selected[step]].className = "option";
        }
        selected[step] = i;
    }
    nextQuestion();
}
function nextQuestion() {
    step++;
    if(step == 10){
        window.location.href = 'result.html?level='+level+"&res=["+selected.toString()+']';
    }
    update();
}
function previousQuestion(){
    step--;
    update();
}
function getParams() {
    var strGET = window.location.search.replace('?', '');
    let arr = strGET.split("=");
    let map = {};
    for (let i = 0; i < arr.length; i += 2) {
        map[arr[i]] = arr[i + 1];
    }
    return map;
}
